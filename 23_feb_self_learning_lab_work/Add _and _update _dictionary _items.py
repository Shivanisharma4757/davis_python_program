# Create dictionary
car = {
    "brand": "Toyota",
    "model": "Corolla",
    "year": 2020
}

# Add new key
car["color"] = "Black"

# Update existing key
car["year"] = 2022

print("Updated dictionary:", car)

#output
#Updated dictionary: {'brand': 'Toyota', 'model': 'Corolla', 'year': 2022, 'color': 'Black'}

# Program to access elements in a tuple

# Create a tuple
numbers = (10, 20, 30, 40, 50)

# Access elements using index
print("First element:", numbers[0])      # Index starts from 0
print("Third element:", numbers[2])

# Access last element using negative index
print("Last element:", numbers[-1])

# Access a range of elements using slicing
print("First three elements:", numbers[0:3])

# output
# First element: 10
#Third element: 30
#Last element: 50
#First three elements: (10, 20, 30)

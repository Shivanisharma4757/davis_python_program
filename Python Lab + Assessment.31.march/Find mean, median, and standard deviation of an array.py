import numpy as np

# Input: NumPy array
arr = np.array([10, 20, 30, 40, 50])

# Calculations
mean = np.mean(arr)
median = np.median(arr)
std_dev = np.std(arr)

# Output
print("Mean:", mean)
print("Median:", median)
print("Standard Deviation:", std_dev)

#output
#Mean: 30.0
#Median: 30.0
#Standard Deviation: 14.142135623730951

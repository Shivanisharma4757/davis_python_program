# Program to print numbers from 1 to N using while loop

n = int(input("Enter value of N: "))   # User se N input lena
i = 1                                 # Counter variable start from 1

while i <= n:                         # Jab tak i N se chota ya equal hai loop chalega
    print(i)                          # Current number print karo
    i += 1                            # i ko 1 se increase karo

# Output Example
# Enter value of N: 5
# 1
# 2
# 3
# 4
# 5

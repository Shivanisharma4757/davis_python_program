# Program to print numbers from 1 to N using while loop

n = int(input("Enter value of N: "))   # Take the value of N from the user
i = 1                                  # Initialize counter variable starting from 1

while i <= n:                          # Loop will run while i is less than or equal to N
    print(i)                           # Print the current value of i
    i = i + 1                          # Increase i by 1

# Output Example
# Enter value of N: 5
# 1
# 2
# 3
# 4
# 5
